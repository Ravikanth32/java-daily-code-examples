package com.patternhub.basics.datatypes;

public class TypeCasting {
    public static void main(String[] args) {
        int x = 130;
        float f = 1220.34f;
        int z = (int) f;
        System.out.println(z);

        byte b= (byte) x;
        System.out.println(b);

        byte b1 = 100;
        int result = b1 * 100;

    }
}
